public class WhileDoWhileStatment
{
	public static void main(String args[])
	{
		/*while loop*/
		int number = 1;
		while(number <= 10)
		{
			System.out.println(number);
			number++;
		}
	
		/*do while loop*/
		int number1 = 1;
		do
		{
			System.out.println(number1);
			number1++;
		}while(number1 <= 10);
	
	}
}
